# For Loops (Looping)

for letra in 'Google':
    print(letra)
print()

livro = 'O Pequeno Principe'

for letra in livro:
    print(letra)
print()

palavra = 'Google'
for letra in palavra:
    print(letra + " está dentro da palavra " + palavra)
print()

for letra in palavra:
    print(f'{letra} está dentro da palavra {palavra}')