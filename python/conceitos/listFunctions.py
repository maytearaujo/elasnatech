#COM LISTA
cidades = ['Rio de Janeiro', 'São Paulo', 'Salvador', 'Goiânia', 'Belo Horizonte']

print(cidades)

# append - adiciona um item no final da lista
cidades.append('Santos')
print(cidades)

# remove - remove um item da lista
cidades.remove('Goiânia')
print(cidades)

# insert insere um item na posição informada
cidades.insert(1, 'Cubatão')
print(cidades)

# pop remove item da posição informada
cidades.pop(4)
print(cidades)

# sort organiza os itens em ordem alfabetica
cidades.sort()
print(cidades)

