num1 = float(input('Informe um número: '))
num2 = float(input('Informe outro número: '))

calculo = num1 + num2
print(f'{num1} + {num2} = {calculo}')

calculo = num1 - num2
print(f'{num1} - {num2} = {calculo}')

calculo = num1 * num2
print(f'{num1} * {num2} = {calculo}')

calculo = num1 / num2
print(f'{num1} / {num2} = {calculo}')

calculo = num1 ** num2
print(f'{num1} ^ {num2} = {calculo}')