# Condiccional statments (Declarações Condicionais)

velocidade = 60;

if velocidade > 110:
    print('Velocidade acima do permitido!, favor reduzir a sua velocidade');
elif velocidade < 60:
    print('Favor dirigir acima de 80 km/h')
else:
    print('Parabéns você está dentro da velocidade permitida');